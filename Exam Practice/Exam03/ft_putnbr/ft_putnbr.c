#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = -nb;
		}
		if (nb == 0)
		{
			write(1, "0", 1);
		}
		if (nb > 0)
		{
			if (nb > 9)
			{
				ft_putnbr(nb / 10);
			}
			nb = nb % 10 + 48;
			write(1, &nb, 1);
		}	
}

int	main(void)
{
	ft_putnbr(-2147483647);
	return (0);
}
